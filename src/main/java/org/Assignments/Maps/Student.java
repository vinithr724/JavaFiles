package org.Assignments.Maps;

// Define a class named Student
public class Student {
    // Instance variables to store student's first name, last name, and GPA
    String firstName;
    String lastName;
    double gpa;

    // Constructor to initialize the Student object with provided first name, last name, and GPA
    public Student(String firstName, String lastName, double gpa) {
        // Assign the provided values to corresponding instance variables
        this.firstName = firstName;
        this.lastName = lastName;
        this.gpa = gpa;
    }
}
